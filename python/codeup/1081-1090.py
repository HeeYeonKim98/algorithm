# 1081 : 주사위 2개를 굴렸을 때 나올 수 있는 수 출력
n,m=map(int, input().split(" "))
for i in range(1,n+1):
    for j in range(1,m+1):
        print(i,j)

# 1082 : 16진수 구구단
a=input()
a=int(a,16)
for i in range(1,16):
    result=a*i
    print("%X*%X=%X" %(a,i,result))

